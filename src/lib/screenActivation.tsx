import { createContext, useContext } from 'react'

/**
 * Лічильник активацій вкладки: зростає щоразу, коли користувач на неї
 * переходить.
 *
 * Потрібен, бо вкладки більше не розмонтовуються при перемиканні (інакше
 * кільця прогресу губили б стан анімації). Через це графіки монтуються
 * один раз на старті й самі по собі вже ніколи не «виростають» заново.
 * Компоненти, яким треба програвати анімацію на кожен вхід у вкладку,
 * підписуються на це значення.
 */
export const ScreenActivationContext = createContext(0)

export const useScreenActivation = (): number => useContext(ScreenActivationContext)
